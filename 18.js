let Marks = 2;
if (Marks >= 90 && Marks == 100) {
    console.log("Grade A");
}
else if (Marks >= 60 && Marks <= 89) {
    console.log("Grade B");
}
else if (Marks >= 36 && Marks <= 59) {
    console.log("Grade C");
}
else if (Marks >= 1 && Marks <= 35) {
    console.log("Grade D");
}
else {
    console.log("invalid");
}